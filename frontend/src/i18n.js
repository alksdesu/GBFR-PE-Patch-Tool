const orderedKeys = Object.keys(translations).filter((key) => key.length >= 2 && key !== '无').sort((a, b) => b.length - a.length)

const formatTokenSource = '%[-+#0-9.*]*[a-zA-Z]'
const formatTokenTestPattern = new RegExp(formatTokenSource)

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}

function compileFormatRule(source, target) {
  const tokenPattern = new RegExp(formatTokenSource, 'g')
  const matches = [...source.matchAll(tokenPattern)]
  if (!matches.length) return null
  let cursor = 0
  let pattern = ''
  matches.forEach((match, index) => {
    pattern += escapeRegExp(source.slice(cursor, match.index))
    const isLastAtEnd = index === matches.length - 1 && match.index + match[0].length === source.length
    pattern += isLastAtEnd ? '(.+)' : '(.+?)'
    cursor = match.index + match[0].length
  })
  pattern += escapeRegExp(source.slice(cursor))
  let replacementIndex = 0
  const replacement = target.replace(new RegExp(formatTokenSource, 'g'), () => `$${++replacementIndex}`)
  try {
    return [new RegExp(pattern, 'g'), replacement]
  } catch {
    return null
  }
}

const formatRules = Object.entries(translations)
