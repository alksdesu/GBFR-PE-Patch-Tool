export function translateText(value) {
  if (value == null) return value
  const input = String(value)
  if (language.value !== 'en' || !/[\u3400-\u9fff]/u.test(input)) return input
  if (Object.prototype.hasOwnProperty.call(translations, input)) return translations[input]

  const leadingWhitespace = input.match(/^\s*/u)?.[0] || ''
  const trailingWhitespace = input.match(/\s*$/u)?.[0] || ''
  const trimmed = input.trim()
  if (trimmed !== input && Object.prototype.hasOwnProperty.call(translations, trimmed)) {
    return leadingWhitespace + translations[trimmed] + trailingWhitespace
  }

  let output = input
  for (const [pattern, replacement] of priorityRules) output = output.replace(pattern, replacement)
  for (const [pattern, replacement] of formatRules) output = output.replace(pattern, replacement)
  for (const [pattern, replacement] of patternRules) output = output.replace(pattern, replacement)
  for (const key of orderedKeys) {
    if (output.includes(key)) output = output.split(key).join(translations[key])
  }
  for (const [pattern, replacement] of patternRules) output = output.replace(pattern, replacement)
  return output
}

let observer
let applying = false
const originalText = new WeakMap()
const originalAttributes = new WeakMap()
const translatedAttributes = ['placeholder', 'title', 'aria-label']

function translateTextNode(node) {
  if (!node || node.nodeType !== Node.TEXT_NODE) return
  const current = node.nodeValue || ''
  if (!/[\u3400-\u9fff]/u.test(current)) return
  if (!originalText.has(node)) originalText.set(node, current)
  const translated = translateText(current)
  if (translated !== current) node.nodeValue = translated
}

function translateElement(element) {
  if (!(element instanceof Element)) return
  let attributeMap = originalAttributes.get(element)
  if (!attributeMap) { attributeMap = new Map(); originalAttributes.set(element, attributeMap) }
  for (const attribute of translatedAttributes) {
    if (!element.hasAttribute(attribute)) continue
    const current = element.getAttribute(attribute) || ''
    if (!/[\u3400-\u9fff]/u.test(current)) continue
    if (!attributeMap.has(attribute)) attributeMap.set(attribute, current)
    const translated = translateText(current)
    if (translated !== current) element.setAttribute(attribute, translated)
  }
  for (const child of element.childNodes) {
    if (child.nodeType === Node.TEXT_NODE) translateTextNode(child)
    else if (child.nodeType === Node.ELEMENT_NODE) translateElement(child)
  }
}

function translateDocument() {
  if (language.value !== 'en' || !document.body) return
  applying = true
  try { translateElement(document.body) } finally { applying = false }
}

export function installI18nObserver() {
  document.documentElement.lang = language.value === 'zh' ? 'zh-CN' : 'en'
  if (language.value !== 'en') return
  const nativeConfirm = window.confirm.bind(window)
  const nativeAlert = window.alert.bind(window)
  window.confirm = (message) => nativeConfirm(translateText(message))
  window.alert = (message) => nativeAlert(translateText(message))
  translateDocument()
  observer = new MutationObserver((mutations) => {
    if (applying || language.value !== 'en') return
    applying = true
    try {
      for (const mutation of mutations) {
        if (mutation.type === 'characterData') translateTextNode(mutation.target)
        else if (mutation.type === 'attributes') translateElement(mutation.target)
        else for (const node of mutation.addedNodes) {
          if (node.nodeType === Node.TEXT_NODE) translateTextNode(node)
          else if (node.nodeType === Node.ELEMENT_NODE) translateElement(node)
        }
      }
    } finally { applying = false }
  })
  observer.observe(document.documentElement, { subtree: true, childList: true, characterData: true, attributes: true, attributeFilter: translatedAttributes })
}
